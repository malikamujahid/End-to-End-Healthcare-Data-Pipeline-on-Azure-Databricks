# DBTITLE 1,build condition summary
from pyspark.sql import Window

# Most common admission type per condition
# Using a window function to rank admission types by frequency
admission_counts = (
    df_gold_base
    .groupBy("medical_condition", "admission_type")
    .agg(F.count("*").alias("admission_count"))
)

window_spec = Window.partitionBy("medical_condition").orderBy(
    F.col("admission_count").desc()
)

most_common_admission = (
    admission_counts
    .withColumn("rank", F.rank().over(window_spec))
    .filter(F.col("rank") == 1)
    .select("medical_condition", 
            F.col("admission_type").alias("most_common_admission_type"))
)

# Core condition aggregations
condition_agg = (
    df_gold_base
    .groupBy("medical_condition")
    .agg(
        F.count("*")
         .alias("total_patients"),
        F.round(F.avg("length_of_stay_days"), 2)
         .alias("avg_length_of_stay_days"),
        F.max("length_of_stay_days")
         .alias("max_length_of_stay_days"),
        F.round(F.avg("billing_amount"), 2)
         .alias("avg_billing_amount"),
        F.round(F.sum("billing_amount"), 2)
         .alias("total_billing_amount"),
        F.round(
            F.sum(F.when(F.col("test_results") == "Abnormal", 1)
                   .otherwise(0)) * 100.0 / F.count("*"), 2
        ).alias("abnormal_test_rate_pct")
    )
)

# Join most common admission type
df_condition_summary = condition_agg.join(
    most_common_admission,
    on="medical_condition",
    how="left"
).orderBy(F.col("total_patients").desc())

print("condition_summary built")
display(df_condition_summary)

