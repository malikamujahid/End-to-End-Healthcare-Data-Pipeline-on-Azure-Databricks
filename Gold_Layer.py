# MAGIC %md
# MAGIC ## Step 3 — Write gold.condition_summary to Delta Lake (Incremental MERGE)
# MAGIC
# MAGIC Writes the condition summary table using a Delta Lake MERGE pattern
# MAGIC rather than a full overwrite.
# MAGIC
# MAGIC **Why MERGE instead of overwrite?**
# MAGIC
# MAGIC Gold tables store aggregations, not individual records. When new patient
# MAGIC admissions arrive in silver, existing condition metrics change. Average
# MAGIC billing shifts, length of stay updates, abnormal test rates fluctuate.
# MAGIC
# MAGIC A full overwrite recalculates everything from scratch every run. This is
# MAGIC correct but inefficient at scale. A MERGE pattern updates only the
# MAGIC conditions where data has changed, leaving untouched conditions as they are.
# MAGIC
# MAGIC This mirrors production-grade pipeline design where Gold tables serve
# MAGIC live dashboards and cannot afford full rebuilds on every pipeline run.
# MAGIC
# MAGIC **MERGE logic:**
# MAGIC - If `medical_condition` already exists in gold → UPDATE all metrics
# MAGIC - If `medical_condition` is new → INSERT as a new row

# COMMAND ----------

# DBTITLE 1,write gold.condition_summary — incremental merge
from delta.tables import DeltaTable

spark.sql("CREATE DATABASE IF NOT EXISTS gold")
print(" Gold database ready")

gold_table_name = "gold.condition_summary"

table_exists = spark.catalog.tableExists(gold_table_name)

if not table_exists:
    (
        df_condition_summary
        .write
        .format("delta")
        .mode("overwrite")
        .option("overwriteSchema", "true")
        .saveAsTable(gold_table_name)
    )
    print(f" {gold_table_name} created on first run")

else:
    delta_table = DeltaTable.forName(spark, gold_table_name)
    (
        delta_table.alias("existing")
        .merge(
            df_condition_summary.alias("updates"),
            "existing.medical_condition = updates.medical_condition"
        )
        .whenMatchedUpdateAll()
        .whenNotMatchedInsertAll()
        .execute()
    )
    print(f"{gold_table_name} updated via MERGE")

# COMMAND ----------

# MAGIC %md
# MAGIC ## Step 4 — Optimise gold.condition_summary
# MAGIC
# MAGIC Runs `OPTIMIZE` on the Gold table to compact small Delta files
# MAGIC into larger, more efficient files for query performance.
# MAGIC
# MAGIC `ZORDER BY medical_condition` co-locates data by condition, the
# MAGIC primary filter used in all downstream queries on this table.
# MAGIC This is a Databricks-specific Delta Lake feature that significantly
# MAGIC reduces query scan time as the dataset grows over time.
# MAGIC
# MAGIC In a production pipeline, OPTIMIZE would be scheduled as a
# MAGIC maintenance job running after each pipeline execution.

